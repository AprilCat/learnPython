x = np.arange(0, 6.28, 0.1)
y1 = np.sin(x)
y2 = np.cos(x)
plt.plot(x, y1, linestyle="--", label="sin")
plt.plot(x, y2, label="cos")
plt.xlabel("x")
plt.ylabel("y")
plt.title("sin and cos")
plt.legend()
plt.show()

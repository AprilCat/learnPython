import numpy as np
import matplotlib.pyplot as plt
from matplotlib import image as img
a = np.array([[1, 3, 5], [2, 4, 6]])
b = np.array([10, 30, 50])
print(a)
print(type(a))
print(a+b)
print(a*b)
print(a/b)
